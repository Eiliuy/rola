/// <summary>
/// 角色外观部位类型
/// </summary>
public enum CharacterPartSlot
{
    Body,        // 身体/肤色基础层
    BackHair,    // 后发（在身体后方）
    Bottom,      // 下装
    Top,         // 上衣/外套
    Gloves,      // 手套
    Shoes,       // 鞋子
    Weapon,      // 武器
    FrontHair,   // 前发（在身体前方）
    Accessory    // 配饰（围巾、披风、眼镜等）
}
